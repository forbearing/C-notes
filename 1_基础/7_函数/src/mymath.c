#include "mymath.h"

/*
 * 函数名称: add
 * 描述: 两个数进行相加后将结果返回
 * 作者: hyb
 * 完成日期: 2017年12月23日
 * 输入: 两个整型数 i 和 j
 * 输出: 整型的两数相加之和
 * 当前版本: 1.0
 */

int add(int i, int j)
{
	return i + j;
}

int sub(int i, int j)
{
	return i - j;
}

int mul(int i, int j)
{
	return i * j;
}

int div(int i, int j)
{
	return i / j;
}
